package javaone;

public class javaaddition {

	  public static void main(String args[])
	  {
		  int num=12;
		  num++;
		  System.out.println(num);
		  ++num;
		  System.out.println(num);
		  
		  int a=num++;
		  System.out.println(a);
		  
		  int b=++num;
		  System.out.println(b);
		  
	  }
}
